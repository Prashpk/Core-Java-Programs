package Projects2;

public class Pattern2 {

	public static void main(String[] args) {
		
		int i,j;
		int k =1;
		
		for(i=1; i<=4; i++)//rows
		{
			for(j=1; j<=i; j++)//cols 
			{
//				
				System.out.print(k+ " ");
				k++;
			}
			System.out.println(" ");
	}
	}
}
	
